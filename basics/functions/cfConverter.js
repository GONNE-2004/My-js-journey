function convertCtoF (celsius) {

  let fahrenheit = celsius * (9/5) + 32;

  return fahrenheit;
}

console.log(convertCtoF(30)); // Output: 86
//0 degrees Celsius is equal to 32 degrees Fahrenheit

//The formula to convert from Celsius to Fahrenheit is the temperature in Celsius multiplied by 9/5, plus 32.